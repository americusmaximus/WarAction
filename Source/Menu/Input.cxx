// 0x10023850
BOOL InitializeDirectInputAction(VOID)
{
    if (DirectInputCreateA(State.Window->Instance, DIRECTINPUT_VERSION, &InputState.Instance, NULL) != DI_OK) { return FALSE; }

    if (InputState.Instance->CreateDevice(GUID_SysKeyboard, &InputState.Device, NULL) != DI_OK) { return FALSE; }

    {
        // 0x1003c014   
        DIDATAFORMAT format;
        ZeroMemory(&format, sizeof(DIDATAFORMAT));

        format.dwSize = sizeof(DIDATAFORMAT);
        format.dwObjSize = sizeof(DIOBJECTDATAFORMAT);
        format.dwFlags = DIDF_RELAXIS;
        format.dwDataSize = MAX_INPUT_STATE_COUNT * sizeof(U8);
        format.dwNumObjs = MAX_INPUT_STATE_COUNT;
        format.rgodf = InputDetails;

        if (InputState.Device->SetDataFormat(&format) != DI_OK) { return FALSE; }
    }

    if (InputState.Device->SetCooperativeLevel(State.Window->HWND, DISCL_FOREGROUND | DISCL_NONEXCLUSIVE) != DI_OK) { return FALSE; }

    InputState.Device->Acquire();

    return TRUE;
}

// 0x10023920
BOOL AcquireDirectInputAction(VOID)
{
    HRESULT result = InputState.Device->GetDeviceState(MAX_INPUT_STATE_COUNT * sizeof(U8), InputState.State);

    while (result == DIERR_NOTACQUIRED || result == DIERR_INPUTLOST)
    {
        result = InputState.Device->Acquire();

        if (result != DI_OK) { ZeroMemory(InputState.State, MAX_INPUT_STATE_COUNT * sizeof(U8));  return TRUE; }

        result = InputState.Device->GetDeviceState(MAX_INPUT_STATE_COUNT * sizeof(U8), InputState.State);
    }

    if (result != DI_OK) { ZeroMemory(InputState.State, MAX_INPUT_STATE_COUNT * sizeof(U8));  return TRUE; }

    return TRUE;
}

// 0x10023970
BOOL ReleaseDirectInputAction(VOID)
{
    if (InputState.Device != NULL)
    {
        InputState.Device->Unacquire();
        DIRECTINPUTRELEASE(InputState.Device);
    }

    DIRECTINPUTRELEASE(InputState.Instance);

    return TRUE;
}

